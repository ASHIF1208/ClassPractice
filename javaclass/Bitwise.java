public class Bitwise{
    public static void main(String[]args){
        int a = 5;
        int b = a&3;
        System.out.println(b);

        int c = 5;
        int d = a|2;
        System.out.println(d);

        int e = 5;
        int f = a^2;
        System.out.println(f);

        int g = 5;
        int h = a>>2;
        System.out.println(h);

        int i = 5;
        int j = a<<2;
        System.out.println(j);
    }
}